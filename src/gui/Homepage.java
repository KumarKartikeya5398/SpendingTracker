    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try
        {
            java.util.Date dt=d.getDate();
            String s1=a.getText();
            String c=(String)cb.getSelectedItem();
            
            if(dt!=null && !s1.equals(" ") && !c.equals(" "))
            {
                java.sql.Date date=new java.sql.Date(dt.getTime());
                int amount=Integer.parseInt(s1);
                db.DbConnect.st.executeUpdate("insert into spendings(category,s_date,amount) values('"+c+"','"+date+"',"+amount+")");
                JOptionPane.showMessageDialog(null,"Expense Added Successfully");
                getEntries();
            }
            else{
                JOptionPane.showMessageDialog(null,"Please Fill All Details");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

