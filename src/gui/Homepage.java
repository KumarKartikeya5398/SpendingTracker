    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        int ri=Table.getSelectedRow();
        if(ri!=-1)
        {
            int r=JOptionPane.showConfirmDialog(null,"Do You Really Want To Delete","Confirmation",JOptionPane.YES_NO_OPTION);
           
           if(r==JOptionPane.YES_OPTION)
           {
            int id=(int)Table.getValueAt(ri,0);
            try {
                db.DbConnect.st.executeUpdate("Delete from spendings where sid="+id+"");
                JOptionPane.showMessageDialog(null,"Deleted Successfully");
                getEntries();
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

